# Perl program to illustrate  
# if - elseif ladder statement 
  
$i = 20;   
  
if($i == 10) 
{ 
	# if - elseif ladder statement
    $i=19; 
} 
elsif($i == 15) 
{ 
	# if - elseif ladder statement
    $i=19;  
} 
elsif($i == 20) 
{ 
	# if - elseif ladder statement
    $i=19;  
} 
else
{ 
	# if - elseif ladder statement
    $i=19;  
} 